using System;

namespace LibraryRago
{
    public class Operation
    {
        public int sumNums(int num1, int num2)
        {
            return num1 + num2;
        }

        public bool isPair(int number)
        {
            return (number % 2 == 0);
        }

        public double sumDecimal(double decim1, double decim2)
        {
            return decim1 + decim2;
        }
    }
